# 从前有座山 山里有座庙  庙里有个小翠花 这个翠花和九妹恩恩爱爱  突然有一天 康俊来了 插到他们中间
# 这里我要表达的一个程序是什么 在指定为止插入一个元素 insert（）

en_ai = ['翠花','九妹']
print('康俊没有出现之前的生活:%s'%en_ai)
# 康俊插足了  意味着幸福美满的生活 过不下去了    （列表里面 可以插入指定元素到指定索引上)
en_ai.insert(1,'康俊')
print('从此三人行:%s'%en_ai)

#翠花怀孕了   志国降临人事3
en_ai.insert(0,'和治国')
print('从此三大一小四人行%s'%en_ai)
