'''
1、函数的定义
定义函数的格式如下
def 函数名():
    函数体（代码)
'''
# demo 
# 定义一个函数，能够完成打印信息的功能
def printInfo():
    print('--------------------------')
    print('人生苦短,我用python')
    print('--------------------------')

# 调用函数
    #定义了函数后，就相当于有了一个具有某些功能的代码，想要让这些代码能够执行，需要调用函数很简单，通过 函数名（）即可完成调用

   # 定义完函数后，函数不会自动执行的，需要调用它才可以
printInfo()
